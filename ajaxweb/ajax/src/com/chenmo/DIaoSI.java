package com.chenmo;

public class DIaoSI {

    private String name;
    private int age;
    private boolean has;
    private Object car;
    private String[] mar;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isHas() {
        return has;
    }

    public void setHas(boolean has) {
        this.has = has;
    }

    public Object getCar() {
        return car;
    }

    public void setCar(Object car) {
        this.car = car;
    }

    public String[] getMar() {
        return mar;
    }

    public void setMar(String[] mar) {
        this.mar = mar;
    }
}
